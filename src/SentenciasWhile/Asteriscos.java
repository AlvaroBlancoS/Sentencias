package SentenciasWhile;

public class Asteriscos {

	public static void main(String[] args) {
		int asterisco = 0;
		// Proceso y salida

		while (asterisco < 5) {

			System.out.print("*");
			System.out.print("*");
			System.out.print("*");
			System.out.print("*");
			System.out.println("*");
			asterisco++;

		}

	}

}
